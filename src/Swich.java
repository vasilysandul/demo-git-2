public class Swich {

        public static void main(String[] args) {

            int a = 1;

            switch (a) {
                case 1:
                    System.out.println("вы ввели один");
                    break;
                case 2:
                    System.out.println("вы ввели два");
                    break;
                case 3:
                    System.out.println("вы ввели три");
                    break;
                case 4:
                    System.out.println("вы ввели четыре");
                    break;
                case 5:
                    System.out.println("вы ввели пять");
                    break;
                default:
                    System.out.println("неверный ввод числа");
            }
        }
    }

