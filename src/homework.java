public class Main {
    public static void main(String[] args) {

        int a = 10000;
        short b = 1000;
        byte c = 100;

        System.out.println("a + b + c = " + (a + b + c));
        System.out.println("a - b - c = " + (a - b - c));
        System.out.println("a * b * c = " + (a * b * c));
        System.out.println("a / b / c = " + (a / b / c));
    }
}
